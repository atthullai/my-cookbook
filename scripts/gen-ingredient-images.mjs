// Generates public/ingredients/*.png (128px, alpha kept) from a source folder
// of raw icons, plus lib/ingredient-image-manifest.ts mapping slug → aliases.
//
// Usage: node scripts/gen-ingredient-images.mjs [srcDir]
//   srcDir defaults to /tmp/ing_icons/ingredients
//
// Filename conventions handled:
//   "name Background Removed.png"  → cut-out variant, preferred over plain
//   "amla or indian gooseberry"    → aliases: amla, indian gooseberry
//   "baking soda : natron"         → aliases: baking soda, natron
//   "steak meat, beef"             → aliases: steak meat, beef
import { execFileSync } from "node:child_process";
import { mkdirSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";

const SRC = process.argv[2] ?? "/tmp/ing_icons/ingredients";
const OUT_DIR = path.join(process.cwd(), "public", "ingredients");
const MANIFEST = path.join(process.cwd(), "lib", "ingredient-image-manifest.ts");

mkdirSync(OUT_DIR, { recursive: true });

const translit = (s) =>
  s
    .normalize("NFC")
    .replace(/ä/g, "a").replace(/ö/g, "o").replace(/ü/g, "u").replace(/ß/g, "ss")
    .replace(/é|è|ê/g, "e").replace(/ñ/g, "n").replace(/ç/g, "c")
    .replace(/Ä/g, "A").replace(/Ö/g, "O").replace(/Ü/g, "U")
    .normalize("NFD").replace(/[̀-ͯ]/g, ""); // strip any remaining combining marks

const slugify = (s) =>
  translit(s.toLowerCase())
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

// 1) Collect entries; prefer "Background Removed" duplicates.
const files = readdirSync(SRC).filter((f) => /\.(png|webp|avif|jpg|jpeg)$/i.test(f));
const bySlug = new Map(); // slug → { file, clean, cutout }
for (const f of files) {
  const base = f.replace(/\.[^.]+$/, "");
  const cutout = /background removed/i.test(base);
  const clean = base.replace(/\s*background removed(\s*\d+)?\s*$/i, "").replace(/\s+/g, " ").trim();
  if (!clean) continue;
  const slug = slugify(clean);
  const existing = bySlug.get(slug);
  if (!existing || (cutout && !existing.cutout)) bySlug.set(slug, { file: f, clean, cutout });
}

// 2) Convert each to 128px png.
let converted = 0, failed = [];
for (const [slug, { file }] of bySlug) {
  const out = path.join(OUT_DIR, `${slug}.png`);
  try {
    execFileSync("sips", ["-Z", "128", "-s", "format", "png", path.join(SRC, file), "--out", out], { stdio: "pipe" });
    converted++;
  } catch {
    failed.push(file);
  }
}

// 3) Manifest: slug → aliases derived from the cleaned filename.
const manifest = {};
for (const [slug, { clean }] of [...bySlug.entries()].sort()) {
  const aliases = new Set();
  const lower = clean.toLowerCase().replace(/\s*\([^)]*\)\s*/g, " ").replace(/\s+/g, " ").trim();
  aliases.add(lower);
  for (const part of lower.split(/\s+or\s+|\s*:\s*|\s*,\s*/)) {
    const p = part.trim();
    if (p) aliases.add(p);
  }
  manifest[slug] = [...aliases];
}

const ts = `// AUTO-GENERATED by scripts/gen-ingredient-images.mjs — do not edit by hand.
// slug → aliases (lowercased) parsed from the source icon filenames.
// Images live at /public/ingredients/<slug>.png (128px, transparent).

export const INGREDIENT_IMAGE_ALIASES: Record<string, string[]> = ${JSON.stringify(manifest, null, 2)};
`;
writeFileSync(MANIFEST, ts);

console.log(`icons: ${bySlug.size} unique (${files.length} source files)`);
console.log(`converted: ${converted}, failed: ${failed.length}`);
if (failed.length) failed.forEach((f) => console.log("  FAILED: " + f));
